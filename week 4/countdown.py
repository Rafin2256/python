# This program counts down from 10 to 1, like a rocket launch countdown.

# Initialize the starting number of the countdown
count = 10

# Start the while loop, which will run as long as count is greater than 0
while count > 0:
    # Print the current count
    print(count)
    
    # Decrease the count by 1 with each loop iteration
    count -= 1

# Once the loop finishes, print the final message
print("Liftoff!")  # This signals the end of the countdown, like a rocket launch

